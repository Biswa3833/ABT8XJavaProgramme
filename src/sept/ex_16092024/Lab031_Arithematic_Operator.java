package sept.ex_16092024;

public class Lab031_Arithematic_Operator {
    public static void main(String[] args) {
        // Arithmetic Operator
        // "+ , _, *, / "
        int a = 10;
        int b = 2;
        System.out.println(a+b);// "+ , - " 2 operands
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        // Arithmetic operators are binary operator which means it require 2 operands .

        // % - is the modulus operator
        // which gives us the Remainders

         //      2 |  10 | 5  ----quotient
         //        |  10 |
        //          -------
        //              0 ----- Remainder
        System.out.println(a%b);

        System.out.println(67%13);

    }
}
