package sept.ex_16092024;

public class Lab038_Relational_Operator {
    public static void main(String[] args) {

        // Relational Operator = boolean
        // > , < , >= <= , != , ! ----> they always return true or false value
        int a = 10;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);

        int age_mamitha = 10;
        int age_pramod = 10;
        boolean result = age_pramod >= age_mamitha; // age_pramod > age_mamitha ot age_pramod = age_mamitha
        System.out.println(result);






    }
}
